        $(document).ready(function(){
            $('.choose').on('change',function(){
                var action = $(this).attr('id');
                var idDistrict = $(this).val();
                var token = $('input[name="_token"]').val();
                var result = '';
                if(action == 'province'){
                    result = 'district';
                }else if(action == 'district'){
                    result = 'commune';
                }
                // console.log(action+"-"+idDistrict+"-"+result);
                $.ajax({
                    url: "{{route('delivery.selectDelivery')}}",
                    method: "POST",
                    headers: {'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')},
                    data: {
                        name:action,
                        district:idDistrict,
                        token:token,
                    },
                    success:function(data){
                        $('#'+result).html(data);
                        // console.log(data);
                    }
                });
            });
            $('.edit-delivery').on('blur',function(){
                var idDelivery = $(this).data('id');
                var feeDelivery = $(this).text();
                var token = $('input[name="_token"]').val();
                $.ajax({
                    url: "{{route('delivery.editDelivery')}}",
                    method: "POST",
                    headers: {'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')},
                    data: {
                        idDelivery:idDelivery,
                        feeDelivery:feeDelivery,
                        token:token,
                    },
                    success:function(data){
                        location.reload();
                        // $('#'+result).html(data);
                        // console.log(data);
                    }
                });
            });
        });
        $(document).ready(function(){
            $('.change-status').change(function(){
                var status = $(this).val();
                var orderId = $(this).children(":selected").data('id');
                var token = $('input[name="_token"]').val();
                var productId = [];
                var quantityOrder = [];
                $('input[name="product_id"]').each(function(){
                    productId.push($(this).val());
                });
                $('.quantity-order').each(function(){
                    quantityOrder.push($(this).text());
                })
                // alert(quantityOrder);
                $.ajax({
                    url: "{{route('order.changeStatus')}}",
                    method: "POST",
                    headers: {'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')},
                    data: {
                        status:status,
                        orderId:orderId,
                        productId:productId,
                        quantityOrder:quantityOrder,
                        token:token,
                    },
                    success:function(data){
                        location.reload();
                        // // $('#'+result).html(data);
                        // console.log(data);
                    }
                });
            });
        });
